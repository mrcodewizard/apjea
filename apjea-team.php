	<script>
		$(window).on("scroll",function(){
			if($(window).scrollTop() >600){
				$(".cabinet-content .inner-column").addClass("fixed-column");
			}
			else{
				$(".cabinet-content .inner-column").removeClass("fixed-column");
			}
			if($(window).scrollTop() > $(".cabinet-content").height()){
				$(".cabinet-content .inner-column").removeClass("fixed-column");
			}
		});

		$(".srch-toggle").on("keyup", function() {
		    var value = $(this).val().toLowerCase();
		    $(".apjea-table tr:not(.profile-tr)").filter(function() {
		      $(this).toggle($(this).text().toLowerCase().indexOf(value) > -1)
		    });
		});

		$("tr.profile-tr").hide();
		$(".apjea-table tr").click(function(){
			$("tr.profile-tr").hide();
			if($(this).next("tr.profile-tr").hasClass("visible")){
				$(this).next("tr.profile-tr").hide(800);
				$(this).next("tr.profile-tr").removeClass("visible");
			}
			else{
				$(this).next("tr.profile-tr").show(800);
				$(this).next("tr.profile-tr").addClass("visible");
			}
		});
	</script>
